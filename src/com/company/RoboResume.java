package com.company;
import java.util.Scanner;
public class RoboResume{
private String name;
private String email_address;
private String education_achievements;
private String experiences;
private String skills_with_rating;

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getEmail_address()
    {
        return email_address;
    }

    public void setEmail_address(String email_address)
    {
        this.email_address = email_address;
    }

    public String getEducation_achievements()
    {
        return education_achievements;
    }

    public void setEducation_achievements(String education_achievements)
    {
        this.education_achievements = education_achievements;
    }

    public String getExperiences()
    {
        return experiences;
    }

    public void setExperiences(String experiences)
    {
        this.experiences = experiences;
    }

    public String getSkills_with_rating()
    {
        return skills_with_rating;
    }

    public void setSkills_with_rating(String skills_with_rating)
    {
        this.skills_with_rating = skills_with_rating;
    }
}


